"use strict";
// 검색창
function showSearch() {
  //document.querySelector("#search_form").style.top = "0";
  document.querySelector("#search_form").style.opacity = "1";
  document.querySelector("#search_form").style.height = "280px";
}

function hideSearch() {
  //document.querySelector("#search_form").style.top = "-999px";
  document.querySelector("#search_form").style.opacity = "1";
  document.querySelector("#search_form").style.height = "0px";
}
